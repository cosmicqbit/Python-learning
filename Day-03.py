# Condtions & Loops

# If
num = 10
if num % 2 == 0:
    print('Num is an even number')

# If else
if True: 
    print('This is the new line')
else:
    print("This line won't execute")

num = 15 
if num % 2 == 0:
    print('The number is even')
else:
    print('The number is odd')

# else if
city = 'Chennai'
if city == 'Bangalore':
    print('City is Bangalore')
elif city == 'Chennai':
    print('City is Chennai')
else:
    print('Not matching')


